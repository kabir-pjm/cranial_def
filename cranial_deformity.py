from tensorflow.keras.models import Model
from tensorflow.keras.layers import Input, Conv3D, BatchNormalization, ReLU, AveragePooling3D, GlobalAveragePooling3D, Dense, Concatenate
from tensorflow.keras.optimizers import Adam

def dense_block(x, num_convs, growth_rate):
    for _ in range(num_convs):
        conv = BatchNormalization()(x)
        conv = ReLU()(conv)
        conv = Conv3D(growth_rate, kernel_size=3, padding='same')(conv)
        x = Concatenate()([x, conv])
    return x

def transition_layer(x, reduction):
    x = BatchNormalization()(x)
    x = ReLU()(x)
    x = Conv3D(int(x.shape[-1] * reduction), kernel_size=1, padding='same')(x)
    x = AveragePooling3D(pool_size=2, strides=2)(x)
    return x

def DenseNet3D(input_shape=(32, 32, 32, 1), num_classes=3, growth_rate=12, num_blocks=3, num_convs_per_block=4, reduction=0.5):
    inputs = Input(shape=input_shape)
    x = Conv3D(2 * growth_rate, kernel_size=3, padding='same')(inputs)

    for _ in range(num_blocks):
        x = dense_block(x, num_convs=num_convs_per_block, growth_rate=growth_rate)
        x = transition_layer(x, reduction)

    x = BatchNormalization()(x)
    x = ReLU()(x)
    x = GlobalAveragePooling3D()(x)
    outputs = Dense(num_classes, activation='softmax')(x)

    model = Model(inputs, outputs)
    model.compile(optimizer=Adam(), loss='categorical_crossentropy', metrics=['accuracy'])
    return model

