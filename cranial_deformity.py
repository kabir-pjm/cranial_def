def resnet_block(input_tensor, filters, kernel_size=(3, 3, 3), strides=(1, 1, 1)):
    x = Conv3D(filters, kernel_size=kernel_size, strides=strides, padding='same')(input_tensor)
    x = BatchNormalization()(x)
    x = ReLU()(x)


    x = Conv3D(filters, kernel_size=kernel_size, strides=strides, padding='same')(x)
    x = BatchNormalization()(x)

    shortcut = Conv3D(filters, kernel_size=(1, 1, 1), strides=strides, padding='same')(input_tensor)
    shortcut = BatchNormalization()(shortcut)


    x = Add()([x, shortcut])
    x = ReLU()(x)
    return x


def create_3d_resnet_model(input_shape=(32, 32, 32, 1), num_classes=10):
    input_tensor = layers.Input(shape=input_shape)


    x = Conv3D(64, kernel_size=(7, 7, 7), strides=(2, 2, 2), padding='same')(input_tensor)
    x = BatchNormalization()(x)
    x = ReLU()(x)
    x = MaxPooling3D(pool_size=(2, 2, 2))(x)


    x = resnet_block(x, 64)
    x = resnet_block(x, 128)


    x = GlobalAveragePooling3D()(x)


    x = Dense(128, activation='relu')(x)
    x = Dense(num_classes, activation='softmax')(x)


    model = models.Model(inputs=input_tensor, outputs=x)


    model.compile(optimizer='adam', loss='categorical_crossentropy', metrics=['accuracy'])

    model.summary()

    return model

